package ua.hillel.malynka.lessons.lesson4;

public class Parallelepiped {
    public static void main(String[] args) {
        int a = 2;
        int b = 8;
        int c = 4;
        int volume = a*b*c;
        int perimeter= (a+b+c)*4;
        System.out.println("Rectangular Parallelepiped with:");
        System.out.println("Side a = "+a);
        System.out.println("Side b = "+b);
        System.out.println("Side c = "+c);
        System.out.println("Volume of Parallelepiped = "+volume);
        System.out.println("Perimeter of Parallelepiped = "+perimeter);
        
    }
}
